const mongoose = require('mongoose');

const teacherRequestSchema = new mongoose.Schema({
  userId: {
    type: mongoose.Schema.Types.ObjectId,
    ref: 'User',
    required: true
  },
  email: {
    type: String,
    required: true
  },
  name: {
    type: String,
    required: true
  },
  status: {
    type: String,
    enum: ['pending', 'approved', 'rejected'],
    default: 'pending'
  },
  requestedAt: {
    type: Date,
    default: Date.now
  },
  reviewedAt: {
    type: Date
  },
  reviewedBy: {
    type: String
  },
  notes: {
    type: String
  }
});

teacherRequestSchema.index({ userId: 1 }, { unique: true });

module.exports = mongoose.model('TeacherRequest', teacherRequestSchema);